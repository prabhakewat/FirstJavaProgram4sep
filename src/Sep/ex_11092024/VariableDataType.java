package Sep.ex_11092024;

public class VariableDataType {

    public static void main(String[]args){

        boolean ATB8x=true;
        System.out.println(ATB8x);

        char Grade = 'A';
        System.out.println(Grade);


        byte age = 110;
        System.out.println(age);


        short largest_no = 2344;
        System.out.println(largest_no);

        int mynum = 2147483647;
        System.out.println(mynum);


        long big_no = 9223372036854775807l;
        System.out.println(big_no);

        float fractional_numbers =5.75f;
        System.out.println(fractional_numbers);


        double d1= 897432E4d;
        System.out.println(d1);


    }
}
