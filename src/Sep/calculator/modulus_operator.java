public class modulus_operator {

    public static void main(String[]args){

        double a = 34;
        double b = 10;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
