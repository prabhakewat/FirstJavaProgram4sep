package Sep.Operators;

public class Unary_Operators {

    public static void main(String[]args){

        /* unary it means one operators if use +65 so + will not show in output only 65
        value will show if use negative value -1 output will show negative */
        int a = +65;  //+ unary operator
        int b = -1;   //- unary operator

        System.out.println(a);
        System.out.println(b);

        b = b+1;
        System.out.println(b);
    }
}
