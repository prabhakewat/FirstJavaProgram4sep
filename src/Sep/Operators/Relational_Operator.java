package Sep.Operators;

public class Relational_Operator {

    public static void main(String[]args){
        // Relation operator whose is give you relation and they always will return some kind of boolean
        // > < , >= <= , != , ! return true/false

        int a = 10;
        int b = 40;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);

        int x = 10;
        int y= 20;
        boolean z = x>y;

        System.out.println(z);



    }

}
