package Sep.ex_16092024;

public class Relational_operators {

    public static void main(String[]args){

        int a = 45;
        int b = 35;

        boolean c = a >b;
        System.out.println(c);

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);

    }
}
